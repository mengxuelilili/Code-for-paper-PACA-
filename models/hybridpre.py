# 假设已定义CombinedModel及相关组件
class CombinedModel(torch.nn.Module):
    def __init__(self, cdr_boundaries_light, cdr_boundaries_heavy):
        super().__init__()
        # 保持与训练阶段完全相同的模型结构
        self.roformer_light = RoformerAttention(embed_dim=532, num_heads=4)
        self.roformer_heavy = RoformerAttention(embed_dim=532, num_heads=4)
        self.global_attention = GlobalAttention(antigen_embed_dim=500)
        self.fusion = MultimodalFusionLayer(
            input_dim_light=532,
            input_dim_heavy=532,
            input_dim_antigen=500,
            hidden_dim=256
        )
        self.affinity_predictor = AffinityPredictorCNNWithAttention()

    def get_fused_features(self, light, heavy, antigen):
        """特征提取方法（添加池化层）"""
        with torch.no_grad():
            # 处理轻链
            light = self.roformer_light(light)
            # 处理重链
            heavy = self.roformer_heavy(heavy)
            # 处理抗原
            antigen = self.global_attention(antigen)
            # 多模态融合
            fused = self.fusion(light, heavy, antigen)
            # 全局平均池化 (batch_size, seq_len, 256) -> (batch_size, 256)
            return torch.mean(fused, dim=1)

